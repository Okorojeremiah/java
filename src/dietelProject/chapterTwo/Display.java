// program that displays the number 1 to 4 on same line

public class Display {
	public static void main(String[] arg) {
		System.out.println("1 2 3 4");

		System.out.print("1 ");
		System.out.print("2 ");
		System.out.print("3 ");
		System.out.print("4");

		System.out.printf("%n%d %d %d %d", 1, 2, 3, 4);
	}
}