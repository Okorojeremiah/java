package com.dietelProject.chapterThree;

public class Calculations {

	
	public int addNumber(int n1, int n2){
	
	return n1 + n2;
	}
	
	public int subtractNumber(int n1, int n2) {
	
	return n1 - n2;
	}

	public int divideNumber(int n1, int n2) {
	
	return n1 / n2;
	}
	
	public int multiplyNumber(int n1, int n2) {
		return n1 * n2;
	}
}